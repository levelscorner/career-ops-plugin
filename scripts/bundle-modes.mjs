#!/usr/bin/env node
// scripts/bundle-modes.mjs
//
// Reads every markdown mode under src/assets/modes/ and emits a typed
// bundle at src/assets/modes.generated.ts that the background service
// worker can import synchronously at startup.
//
// The output looks like:
//
//   export type ModeName = 'evaluate' | 'compare' | ...;
//   export type ModeLanguage = 'en' | 'de' | 'fr' | 'pt';
//   export interface Mode { name: string; language: ModeLanguage; markdown: string; }
//   export const MODES: Record<string, Mode> = { ... };
//
// Keyed by `${language}:${name}` so the loader can look up `en:evaluate`
// etc. in O(1).

import { readdirSync, readFileSync, writeFileSync, statSync, mkdirSync } from 'node:fs';
import { join, dirname, basename, extname, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const MODES_DIR = join(ROOT, 'src/assets/modes');
const OUT_FILE = join(ROOT, 'src/assets/modes.generated.ts');

const LANG_DIRS = new Set(['de', 'fr', 'pt', 'es']);

/**
 * Recursively collect every .md file under MODES_DIR and classify it by
 * language (top-level = 'en', subfolders = their 2-letter code).
 */
function collectModes() {
  const modes = [];
  const walk = (dir, language) => {
    for (const entry of readdirSync(dir)) {
      const full = join(dir, entry);
      const st = statSync(full);
      if (st.isDirectory()) {
        if (LANG_DIRS.has(entry)) {
          walk(full, entry);
        }
        continue;
      }
      if (extname(entry) !== '.md') continue;
      const name = basename(entry, '.md');
      // Skip non-runtime files: READMEs and the _profile template scaffold.
      if (name === 'README') continue;
      if (name === '_profile.template') continue;
      const markdown = readFileSync(full, 'utf8');
      modes.push({ name, language, markdown, source: relative(ROOT, full) });
    }
  };
  walk(MODES_DIR, 'en');
  return modes;
}

function escapeTemplate(s) {
  // Escape backticks, ${, and backslashes for use inside a TS template literal.
  return s.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
}

function generate() {
  const modes = collectModes();
  modes.sort((a, b) => (a.language + a.name).localeCompare(b.language + b.name));

  const names = [...new Set(modes.map((m) => m.name))].sort();
  const languages = [...new Set(modes.map((m) => m.language))].sort();

  let out = '';
  out += '// AUTO-GENERATED by scripts/bundle-modes.mjs — DO NOT EDIT BY HAND.\n';
  out += '// Edit src/assets/modes/**/*.md instead and rerun `npm run modes:bundle`.\n\n';
  out += `export type ModeName = ${names.map((n) => `'${n}'`).join(' | ')};\n`;
  out += `export type ModeLanguage = ${languages.map((l) => `'${l}'`).join(' | ')};\n\n`;
  out += 'export interface Mode {\n';
  out += '  name: ModeName;\n';
  out += '  language: ModeLanguage;\n';
  out += '  markdown: string;\n';
  out += '  source: string;\n';
  out += '}\n\n';
  out += 'export const MODES: Readonly<Record<string, Mode>> = Object.freeze({\n';
  for (const mode of modes) {
    const key = `${mode.language}:${mode.name}`;
    out += `  ${JSON.stringify(key)}: {\n`;
    out += `    name: ${JSON.stringify(mode.name)},\n`;
    out += `    language: ${JSON.stringify(mode.language)},\n`;
    out += `    source: ${JSON.stringify(mode.source)},\n`;
    out += `    markdown: \`${escapeTemplate(mode.markdown)}\`,\n`;
    out += '  },\n';
  }
  out += '});\n\n';
  out += 'export const MODE_COUNT = ' + modes.length + ';\n';

  mkdirSync(dirname(OUT_FILE), { recursive: true });
  writeFileSync(OUT_FILE, out, 'utf8');
  // eslint-disable-next-line no-console
  console.log(
    `[bundle-modes] wrote ${modes.length} modes (${languages.length} langs, ${names.length} names) → ${relative(ROOT, OUT_FILE)}`,
  );
}

generate();
